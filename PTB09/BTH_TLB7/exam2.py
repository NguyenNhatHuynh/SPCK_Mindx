# Áp dụng cấu trúc tuần tự
name = input("Hãy nhập tên của bạn: ")
math = float(input("Điểm toán của bạn là: "))
english = float(input("Điểm tiếng Anh của bạn là: "))
informatics = float(input("Điểm tin học của bạn là: "))
gpa = (math + english + informatics) / 3
print("Xin chào", name)
print("Điểm trung bình 3 môn học của bạn là:", gpa)

